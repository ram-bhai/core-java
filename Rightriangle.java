//To print right triangle star pattern.

class Rightriangle
  {
    public static void main(java.lang.String args[])
      {
         int i,j;
          for(i=1;i<=9;i++)
          {
            for(j=1;j<=i;j++)
            {
             System.out.printf("*");
            }
             System.out.printf("\n");
          }
     }
 }

