//to print data pattern from 1 to 5.

class Dptrn
   {
     public static void main(java.lang.String args[])
      {
         int i,j;
          for(i=1;i<=5;i++)
          {
           for(j=1;j<=i;j++)
           {
             System.out.printf("%d",j);
            } 
             System.out.printf("\n");
           }
        }
   }

